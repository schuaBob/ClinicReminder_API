package com.huaclinic.restfulapi.payload.response;

public class PatientRes {
    private Integer id;
    private String name;
    
    public PatientRes(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
